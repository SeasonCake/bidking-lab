# Collapse ``优品均格：优品均格`` style duplicates after table fixes.
_RE_TOOL_LABEL_DUP = re.compile(
    r"(优品均格|极品均格|珍品均格|优品估价|极品估价)(：\s*\1)+",
)


def normalize_ocr_text(text: str) -> str:
    """Apply lightweight string fixes; does not change layout."""
    if not text:
        return text
    out = text.replace("\r\n", "\n")
    for wrong, right in _OCR_CHAR_FIXES:
        out = out.replace(wrong, right)
    out = _RE_TOOL_LABEL_DUP.sub(r"\1", out)
    return out
